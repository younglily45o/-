shop = document.getElementsByClassName("addshopping");
shop[0].onclick = function() {
	flag = 0
	localStorage.setItem("flag0", flag);
	zindex = document.getElementById("fff_text")
	zindex.style.zIndex = 100;
	zindex.style.animationPlayState = "running"
	setTimeout(function() {
		zindex.style.zIndex = -1;
		zindex.style.animationPlayState = "paused"
	}, 2000);
}
